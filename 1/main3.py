# целое число - integer - int
num1 = 123_475_673
# + - * / // % **
print(num1 ** 2)
print(num1 % 24567)
print(type(num1))
# дробные числа - floating point - float
# + - * / // % **
num2 = 56245.245346
print(type(num2))
# строка - string - str
s = "string"
s2 = 'new'
# + конкатенация строк
print(s + ' ' + s2)
print(s2 + '!' + s)
# * дублирование
print(s * 3)
# логический тип данных - boolean - bool
a = True
b = False
# not and or
print(not a)
print(not b)
print(a and b)
print(a or b)


